package msd.conestoga.org.qalab3.business;

public class Calculator {

    public Calculator() {

    }

    public int multiply(int lhs, int rhs) {
        return rhs * lhs;
    }

    public int subtract(int lhs, int rhs) {
        return lhs - rhs;
    }

    public int add(int lhs, int rhs) {
        return lhs + rhs;
    }

    public int divide(int lhs, int rhs) {
        return lhs / rhs;
    }
}
